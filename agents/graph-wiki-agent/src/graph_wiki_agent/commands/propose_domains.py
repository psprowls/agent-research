def _resolve_paths(workspace_arg: str) -> tuple[Path, Path]:
    """Resolve (repo_root, workspace) from --workspace arg or GRAPH_WIKI_WORKSPACE."""
    if workspace_arg:
        cfg = resolve_config(Path(workspace_arg).resolve(), require_manifest=False)
    else:
        cfg = resolve_config(None, require_manifest=False)
    return cfg.repo_root, cfg.workspace


def _aggregate_fan_out(
    fan_result: FanOutResult,
) -> tuple[list[ProposedDomain], list[str]]:
    """Split FanOutResult into proposed domains + cluster-name failure list."""
    proposed: list[ProposedDomain] = []
    failures: list[str] = []
    for item, value in fan_result.successes:
        if isinstance(value, ProposedDomain):
            proposed.append(value)
        else:
            # _parse_tool_call returned None ŌĆö count as a soft failure.
            cname = getattr(item, "name", str(item))
            failures.append(cname)
    for err in fan_result.errors:
        cname = getattr(err.item, "name", str(err.item))
        failures.append(cname)
    return proposed, failures


def propose_domains_cmd(
    workspace: str = typer.Option(
        "", "--workspace", help="Workspace root (defaults to GRAPH_WIKI_WORKSPACE)"
    ),
    hub_threshold: float = typer.Option(
        0.5, "--hub-threshold", help="Fraction-of-packages threshold for cross-cutting hub detection"
    ),
    model: Optional[str] = typer.Option(
        None, "--model", help="Override domain-proposer model_id (D-21)"
    ),
) -> None:
    """Propose candidate domains from cg domain-clusters via an LLM fan-out.

    Writes ``<repo_root>/domains.proposed.yaml`` (next to ``domains.yaml``)
    for human review. NEVER auto-applies; the user reviews, edits, and
    (optionally) merges into the authoritative ``domains.yaml``.
    """
    repo_root, workspace_root = _resolve_paths(workspace)

    # D-23: in-process compute_clusters ŌĆö NOT a subprocess to cg.
    db_path = graph_dir(workspace_root) / "code.db"
    try:
        conn = read_only_connect(db_path)
    except GraphNotInitializedError as exc:
        typer.echo(f"error: graph not initialized: {exc}", err=True)
        raise typer.Exit(code=2)

    cluster_result: ClusterResult = compute_clusters(
        conn, hub_threshold=hub_threshold
    )

    # Existing domains (for parent options + cycle-immune edges).
    # `domains.yaml` lives at the REPO root (same path `graph_io.domains.emit`
    # reads it from); the workspace is the `.graph-wiki/` directory.
    existing = _load_existing_domains(repo_root)
    existing_edges = _existing_parent_edges(existing)
    existing_domain_names = tuple(sorted(existing.keys()))

    # D-09 grounding set.
    valid_packages = {n.name for n in list_packages(conn)}

    # D-03: hubs-by-cluster-id (invert CrossCuttingHub.connects_clusters).
    hubs_by_cluster_id: dict[int, list[str]] = {}
    for hub in cluster_result.cross_cutting:
        for cid in hub.connects_clusters:
            hubs_by_cluster_id.setdefault(cid, []).append(hub.name)
    hubs_by_cluster_id_tuple = {
        cid: tuple(sorted(names)) for cid, names in hubs_by_cluster_id.items()
    }

    # D-19/D-21: LLM construction.
    llm = make_llm("domain-proposer", model_override=model)
    role_cfg = load_role_config("domain-proposer")
    effective_model_id = model if model is not None else role_cfg["model_id"]
    bound_llm = llm.bind_tools([_PROPOSE_DOMAIN_TOOL])

    # D-02: per-package context loader. v1.8 leaves this minimal ŌĆö the
    # planner intentionally deferred richer summary/file_map wiring to v1.9
    # eval. A no-op loader keeps the prompt focused on package names.
    def _empty_context(_pkg_name: str) -> dict:
        return {}

    cluster_task = _make_cluster_task(
        hubs_by_cluster_id=hubs_by_cluster_id_tuple,
        context_loader=_empty_context,
        existing_domain_names=existing_domain_names,
        bound_llm=bound_llm,
    )

    # D-01/D-20: SubagentPool fan-out ŌĆö partial-success + trace records.
    pool = SubagentPool(trace_dir=workspace_root / ".graph-wiki" / "traces")
    fan_result = asyncio.run(
        pool.run_all(
            items=list(cluster_result.clusters),
            task=cluster_task,
            role="domain-proposer",
            model_id=effective_model_id,
            max_concurrency=role_cfg["max_concurrency"],
        )
    )

    proposed_list, llm_failures = _aggregate_fan_out(fan_result)

    # D-07: mechanical cross-cutting domain.
    cross_cutting_domain = _build_cross_cutting_domain(cluster_result.cross_cutting)
    if cross_cutting_domain is not None:
        proposed_list.append(cross_cutting_domain)

    # D-09: ground against list_packages.
    proposed_tuple, stripped_unknown = _strip_unknown_packages(
        tuple(proposed_list), valid_packages
    )

    # D-10/D-11/D-12: cycle-strip proposed parent edges.
    proposed_edges = [
        (d.name, d.parent) for d in proposed_tuple if d.parent
    ]
    kept_edges, stripped_cycle = _strip_cycle_edges(proposed_edges, existing_edges)
    kept_edge_set = set(kept_edges)
    stripped_edge_set = set(stripped_cycle)
    # Null-out parent on any domain whose edge was stripped.
    if stripped_edge_set:
        rebuilt: list[ProposedDomain] = []
        for d in proposed_tuple:
            if d.parent and (d.name, d.parent) in stripped_edge_set:
                rebuilt.append(
                    ProposedDomain(
                        name=d.name,
                        packages=d.packages,
                        parent=None,
                        description=d.description,
                        confidence=d.confidence,
                        llm_origin=d.llm_origin,
                    )
                )
            else:
                rebuilt.append(d)
        proposed_tuple = tuple(rebuilt)

    # D-20: total_cost_usd ŌĆö best-effort sum from the FanOutResult trace
    # records. v1.8 leaves this at 0.0 because the trace-cost-aggregation
    # helper landed in subagent_runtime's per-call writer (D-20: "written
    # automatically by SubagentPool.run_all"); a separate aggregator across
    # the JSONL trace can be added in v1.9 without changing this schema.
    total_cost = 0.0
    _ = kept_edge_set  # kept_edges is implicit in proposed_tuple's parent fields

    result = ProposeResult(
        proposed_domains=tuple(
            sorted(proposed_tuple, key=lambda d: d.name)
        ),
        stripped_unknown_packages=stripped_unknown,
        stripped_cycle_edges=tuple(stripped_cycle),
        llm_failures=tuple(llm_failures),
        total_cost_usd=total_cost,
    )

    # D-13/D-14/D-15/D-16: write the artifact (overwrite).
    # Output sits next to `domains.yaml` at the repo root so reviewers can
    # diff the two side-by-side. The workspace is reserved for derived
    # `.graph-wiki/` state.
    output_path = repo_root / "domains.proposed.yaml"
    _write_proposed_yaml(
        result,
        output_path,
        cluster_command=f"cg domain-clusters --hub-threshold {hub_threshold}",
        model=effective_model_id,
    )

    typer.echo(f"wrote {output_path}")
    if llm_failures:
        typer.echo(
            f"warning: {len(llm_failures)} cluster(s) failed: "
            f"{', '.join(llm_failures)}",
            err=True,
        )
