# --------------------------------------------------------------------------- #
# Dataclasses (D-06)
# --------------------------------------------------------------------------- #


@dataclass(frozen=True)
class ProposedDomain:
    """One LLM-proposed (or mechanically-built) domain candidate."""

    name: str
    packages: tuple[str, ...]  # sorted
    parent: Optional[str]
    description: str
    confidence: float
    llm_origin: str  # "fan_out" or "cross_cutting"


@dataclass(frozen=True)
class ProposeResult:
    """Aggregate result of a `graph propose-domains` run."""

    proposed_domains: tuple[ProposedDomain, ...]  # sorted by name
    stripped_unknown_packages: tuple[str, ...]  # PROPOSE-02 strips
    stripped_cycle_edges: tuple[tuple[str, str], ...]  # PROPOSE-03 strips (child, parent)
    llm_failures: tuple[str, ...]  # cluster names whose LLM call failed
    total_cost_usd: float  # PROPOSE-06 aggregated cost (best-effort)


# --------------------------------------------------------------------------- #
# Tool-call parsing (D-05)
# --------------------------------------------------------------------------- #


def _parse_tool_call(resp: Any) -> Optional[ProposedDomain]:
    """Extract the first ``propose_domain`` tool call from an LLM response.

    Returns None when the response has no tool calls or no matching call.
    The returned ``ProposedDomain`` carries ``llm_origin='fan_out'`` (D-06).
    Packages are sorted (deterministic byte-output downstream ŌĆö D-15).
    """
    tool_calls = getattr(resp, "tool_calls", None) or []
    for call in tool_calls:
        if call.get("name") != "propose_domain":
            continue
        args = call.get("args") or {}
        packages = tuple(sorted(args.get("packages", []) or []))
        return ProposedDomain(
            name=args["name"],
            packages=packages,
            parent=args.get("parent"),
            description=args["description"],
            confidence=float(args["confidence"]),
            llm_origin="fan_out",
        )
    return None


# --------------------------------------------------------------------------- #
# Grounding (D-09)
# --------------------------------------------------------------------------- #


def _strip_unknown_packages(
    proposed: tuple[ProposedDomain, ...],
    valid_packages: set[str],
) -> tuple[tuple[ProposedDomain, ...], tuple[str, ...]]:
    """Filter each proposed domain's packages against ``valid_packages``.

    A domain whose packages are ALL stripped is itself dropped (claude-discretion
    lean from CONTEXT). Per-strip stderr warning is emitted via ``typer.echo(...,
    err=True)``. Returns ``(kept_domains, sorted_stripped_names)``.
    """
    kept: list[ProposedDomain] = []
    stripped: set[str] = set()
    for domain in proposed:
        filtered = tuple(sorted(p for p in domain.packages if p in valid_packages))
        for missing in domain.packages:
            if missing not in valid_packages:
                stripped.add(missing)
                typer.echo(
                    f"warning: stripping unknown package '{missing}' proposed "
                    f"for domain '{domain.name}' ŌĆö not in list_packages output",
                    err=True,
                )
        if not filtered:
            # D-09 + claude-discretion: drop domain entirely if no packages
            # survive grounding.
            continue
        kept.append(
            ProposedDomain(
                name=domain.name,
                packages=filtered,
                parent=domain.parent,
                description=domain.description,
                confidence=domain.confidence,
                llm_origin=domain.llm_origin,
            )
        )
    return tuple(kept), tuple(sorted(stripped))


# --------------------------------------------------------------------------- #
# Cycle detection (D-10, D-11, D-12)
# --------------------------------------------------------------------------- #


def _strip_cycle_edges(
    proposed_edges: list[tuple[str, str]],
    existing_edges: list[tuple[str, str]],
) -> tuple[list[tuple[str, str]], list[tuple[str, str]]]:
    """Strip cycle-introducing edges from ``proposed_edges``.

    Operates on ``union(proposed, existing)`` (D-10). Existing edges are
    immune (D-11) ŌĆö only proposed edges may be removed. Cycle detection is
    iterative-DFS grey/black coloring (D-12; no recursion, no networkx).

    Determinism (D-15): ``proposed_edges`` are sorted lexicographically before
    the first scan so the first proposed edge encountered on a cycle is
    invariant across runs. ``existing_edges`` are also sorted to make the
    DFS path deterministic.

    Returns ``(kept_proposed, stripped_proposed)``; both are lists of
    ``(child, parent)`` tuples.
    """
    proposed = sorted(proposed_edges)
    existing_sorted = sorted(existing_edges)
    stripped: list[tuple[str, str]] = []

    while True:
        # Mark each edge with origin so we can identify a strippable one.
        # 'proposed' edges are strippable; 'existing' edges are immune.
        edges: list[tuple[str, str, str]] = [
            (c, p, "existing") for c, p in existing_sorted
        ] + [(c, p, "proposed") for c, p in proposed]

        # Build adjacency: child -> list of (parent, origin, edge_index)
        # We keep the edges sorted-then-stable so adjacency iteration order is
        # deterministic. Index into `edges` lets us recover the edge tuple
        # later if needed.
        adj: dict[str, list[tuple[str, str]]] = {}
        nodes: set[str] = set()
        for c, p, origin in edges:
            adj.setdefault(c, []).append((p, origin))
            nodes.add(c)
            nodes.add(p)

        # Iterative DFS with grey/black coloring.
        WHITE, GREY, BLACK = 0, 1, 2
        color = {n: WHITE for n in nodes}
        # Stack entries: (node, iterator over outgoing edges)
        # First proposed edge encountered on a back-edge cycle is the strip target.
        first_proposed_on_cycle: Optional[tuple[str, str]] = None

        for start in sorted(nodes):
            if color[start] != WHITE:
                continue
            # Track path so we can locate the first proposed edge along it.
            path: list[tuple[str, str, str]] = []
            # (node, iterator)
            stack: list[tuple[str, Any]] = [(start, iter(sorted(adj.get(start, []))))]
            color[start] = GREY

            while stack:
                node, it = stack[-1]
                try:
                    nxt, origin = next(it)
                except StopIteration:
                    color[node] = BLACK
                    stack.pop()
                    if path:
                        path.pop()
                    continue

                if color.get(nxt, WHITE) == GREY:
                    # Back-edge ŌĆö cycle. The path from `nxt` back to `node`
                    # plus the edge (node, nxt) is the cycle. Find the first
                    # proposed edge along that cycle path; existing edges are
                    # immune.
                    # path holds (parent_node, child_node, origin) for each
                    # edge we've descended into. The closing edge is
                    # (node, nxt, origin).
                    closing_edge = (node, nxt, origin)
                    # Cycle edges: walk back through `path` until we re-find
                    # `nxt` as a source, then take the closing edge.
                    cycle_edges: list[tuple[str, str, str]] = []
                    for src, dst, o in reversed(path):
                        cycle_edges.append((src, dst, o))
                        if src == nxt:
                            break
                    cycle_edges.reverse()
                    cycle_edges.append(closing_edge)
                    for src, dst, o in cycle_edges:
                        if o == "proposed":
                            first_proposed_on_cycle = (src, dst)
                            break
                    if first_proposed_on_cycle is None:
                        # Cycle in existing edges only ŌĆö pre-existing problem
                        # (D-11); we cannot strip it. Bail out to avoid an
                        # infinite loop.
                        break
                    # Strip and restart.
                    break

                if color.get(nxt, WHITE) == WHITE:
                    color[nxt] = GREY
                    path.append((node, nxt, origin))
                    stack.append((nxt, iter(sorted(adj.get(nxt, [])))))
                    continue

                # BLACK: already finished ŌĆö no cycle via this edge.
                # (no action ŌĆö outer loop next() will advance)

            if first_proposed_on_cycle is not None:
                break  # restart outer while loop with the edge removed

        if first_proposed_on_cycle is None:
            # No (strippable) cycle remains.
            return proposed, stripped

        # Strip the identified proposed edge and restart.
        stripped.append(first_proposed_on_cycle)
        typer.echo(
            "warning: stripping cycle-introducing parent edge: "
            f"'{first_proposed_on_cycle[0]}' -> '{first_proposed_on_cycle[1]}'",
            err=True,
        )
        proposed = [e for e in proposed if e != first_proposed_on_cycle]


# --------------------------------------------------------------------------- #
# Cross-cutting builder (D-07, D-08)
# --------------------------------------------------------------------------- #


def _build_cross_cutting_domain(
    cross_cutting: tuple[CrossCuttingHub, ...],
) -> Optional[ProposedDomain]:
    """Build the mechanical `cross-cutting` proposed domain (D-07).

    Returns None when the input tuple is empty (claude-discretion lean).
    No LLM call: name is hardcoded (D-08), confidence=1.0, llm_origin='cross_cutting'.
    """
    if not cross_cutting:
        return None
    packages = tuple(sorted(h.name for h in cross_cutting))
    return ProposedDomain(
        name=_CROSS_CUTTING_NAME,
        packages=packages,
        parent=None,
        description=_CROSS_CUTTING_DESCRIPTION,
        confidence=1.0,
        llm_origin="cross_cutting",
    )


