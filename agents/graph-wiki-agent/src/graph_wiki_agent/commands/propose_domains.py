# Per-cluster prompt + fan-out task (D-01..D-04)
# --------------------------------------------------------------------------- #


def _build_cluster_prompt(
    cluster: Cluster,
    *,
    hubs_used: tuple[str, ...],
    context_loader: Callable[[str], dict],
    existing_domain_names: tuple[str, ...],
) -> str:
    """Compose the human prompt for one cluster (D-02/D-03/D-04).

    `context_loader(pkg_name)` returns ``{"summary": str, "file_map": str}``.
    """
    package_blocks: list[str] = []
    for pkg_name in cluster.members:
        ctx = context_loader(pkg_name) if context_loader else {}
        summary = ctx.get("summary") or "<no summary available>"
        file_map = ctx.get("file_map") or ""
        block = f"- {pkg_name}: {summary}"
        if file_map:
            block += f"\n  File map:\n{file_map}"
        package_blocks.append(block)

    hubs_section = ""
    if hubs_used:
        hub_lines = "\n".join(f"- {h}" for h in hubs_used)
        hubs_section = (
            "\n\n## Cross-cutting hubs this cluster uses\n" + hub_lines
        )

    parent_options = (
        ", ".join(existing_domain_names) if existing_domain_names else "none"
    )

    return (
        "You are proposing a name and description for a candidate domain in "
        "a Python monorepo.\n\n"
        "The domain candidate contains these packages:\n\n"
        + "\n\n".join(package_blocks)
        + hubs_section
        + f"\n\nOptionally, you may propose a parent domain (one of: "
        f"{parent_options}).\n\n"
        "Use the propose_domain tool to return your proposal."
    )


def _make_cluster_task(
    *,
    hubs_by_cluster_id: dict[int, tuple[str, ...]],
    context_loader: Callable[[str], dict],
    existing_domain_names: tuple[str, ...],
    bound_llm: Any,
) -> Callable[[Cluster], Awaitable[TaskResult]]:
    """Return the per-cluster async task closure for SubagentPool (D-01)."""

    async def _propose_for_cluster(cluster: Cluster) -> TaskResult:
        prompt = _build_cluster_prompt(
            cluster,
            hubs_used=hubs_by_cluster_id.get(cluster.id, ()),
            context_loader=context_loader,
            existing_domain_names=existing_domain_names,
        )
        resp = await bound_llm.ainvoke([HumanMessage(content=prompt)])
        return TaskResult(value=_parse_tool_call(resp), response=resp)

    return _propose_for_cluster


