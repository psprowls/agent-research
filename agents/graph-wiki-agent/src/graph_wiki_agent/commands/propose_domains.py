# Existing-domains loader (RESEARCH F-6 + CONTEXT D-04/D-10)
# --------------------------------------------------------------------------- #


def _load_existing_domains(workspace_root: Path) -> dict[str, dict]:
    """Return the existing ``domains.yaml`` mapping (``{name: info_dict}``).

    Missing file ŌåÆ ``{}`` (no error). Accepts BOTH shapes:
      - FLAT (as `cg update` consumes; e.g.
        `core: {packages: [foo]}\\nweb: {packages: [bar]}`)
      - NESTED (`{"domains": {core: ..., web: ...}}`)

    Returns the per-domain mapping (`{name: info_dict}`). Returns ``{}`` if
    the file is unreadable, malformed, or empty.
    """
    path = workspace_root / "domains.yaml"
    if not path.exists():
        return {}
    try:
        with path.open("r", encoding="utf-8") as f:
            data = yaml.safe_load(f) or {}
    except yaml.YAMLError as exc:
        typer.echo(f"warning: domains.yaml: parse error: {exc}", err=True)
        return {}
    if not isinstance(data, dict):
        return {}
    # Prefer nested shape if the top-level has a single `domains:` key that
    # maps to a dict-of-dicts ŌĆö that's the explicit form. Otherwise treat
    # the file as flat (matches `graph_io.domains.emit` which iterates
    # `data.items()` directly).
    inner = data.get("domains")
    if isinstance(inner, dict) and all(
        isinstance(v, dict) for v in inner.values()
    ):
        return inner
    # Flat: each top-level key is a domain name mapping to its info dict.
    return {k: v for k, v in data.items() if isinstance(v, dict)}


def _existing_parent_edges(existing: dict[str, dict]) -> list[tuple[str, str]]:
    """Build a sorted ``[(child, parent), ...]`` list from existing domains.

    Used as the immune-edge set in cycle detection (D-11).
    """
    edges: list[tuple[str, str]] = []
    for child, info in existing.items():
        if not isinstance(info, dict):
            continue
        parent = info.get("parent")
        if parent:
            edges.append((child, parent))
    return sorted(edges)


# --------------------------------------------------------------------------- #
# YAML writer (D-14, D-15, D-16)
# --------------------------------------------------------------------------- #


def _write_proposed_yaml(
    result: ProposeResult,
    output_path: Path,
    *,
    cluster_command: str,
    model: str,
) -> None:
    """Write the proposal artifact to ``output_path`` (D-13/D-16: overwrite).

    Format (D-14):
      Line 1: banner comment
      Body:   yaml.safe_dump({"proposed_domains": {...}, "metadata": {...}},
                              sort_keys=True, default_flow_style=False)

    ``proposed_domains`` is a dict keyed by domain name; each value has
    ``packages, description, parent, confidence, llm_origin``.
    """
    proposed_dict: dict[str, dict] = {}
    for d in sorted(result.proposed_domains, key=lambda x: x.name):
        proposed_dict[d.name] = {
            "packages": list(d.packages),
            "description": d.description,
            "parent": d.parent,
            "confidence": d.confidence,
            "llm_origin": d.llm_origin,
        }

    metadata = {
        "generated_at": datetime.datetime.now(tz=datetime.timezone.utc).strftime(
            "%Y-%m-%dT%H:%M:%SZ"
        ),
        "cluster_command": cluster_command,
        "model": model,
        "total_cost_usd": float(result.total_cost_usd),
        "stripped_unknown_packages": list(result.stripped_unknown_packages),
        "stripped_cycle_edges": [list(pair) for pair in result.stripped_cycle_edges],
        "llm_failures": list(result.llm_failures),
    }

    payload = {
        "proposed_domains": proposed_dict,
        "metadata": metadata,
    }

    body = yaml.safe_dump(payload, sort_keys=True, default_flow_style=False)
    with output_path.open("w", encoding="utf-8") as f:
        f.write(_BANNER + "\n")
        f.write(body)


